# Take a number as string input from the user, then convert it to: Integer, Float, Complex Number then print each converted values with its type.
a = input("Enter a number : ")
INT = int(a)
FLOAT = float(a)
COMPLEX_NUM = complex(a)

print(INT)
print(FLOAT)
print(COMPLEX_NUM)

print (type(INT))
print(type(FLOAT))
print(type(COMPLEX_NUM))


